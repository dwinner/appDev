namespace DocComments
{
   /// <summary>
   /// Предоставляет метод для сложения двух целочисленных значений
   /// </summary>
   public class MathLib
   {
      /// <summary>
      /// Метод Add позволяет сложить два целых числа
      /// </summary>
      /// <param name="x">Первое число для сложения</param>
      /// <param name="y">Второе число для сложения</param>
      /// <returns>Результат сложения</returns>
      public int Add(int x, int y)
      {
         return x + y;
      }
   }
}