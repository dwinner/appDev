namespace Adu.DrawingApp.Poco
{
   /// <summary>
   ///    Structure of inscribing rectangle
   /// </summary>
   public struct Inscriber
   {
      /// <summary>
      ///    Width of inscribing rectangle
      /// </summary>
      public double Width { get; set; }

      /// <summary>
      ///    Height of inscribing rectangle
      /// </summary>
      public double Height { get; set; }

      /// <summary>
      ///    Left coordinate value of inscribing rectangle
      /// </summary>
      public double Left { get; set; }

      /// <summary>
      ///    Top coordinate value of inscribing rectangle
      /// </summary>
      public double Top { get; set; }
   }
}